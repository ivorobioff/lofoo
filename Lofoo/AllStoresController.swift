    var stores: [[String:String]] = [
        ["name": "Fidesco", "location": "123 Miorita"],
        ["name": "Nr.1", "location": "28 Ion Creanga"]
    ]
    
    
    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false
        
        let addButton = UIBarButtonItem(barButtonSystemItem: .Add, target: self, action: "addStore")
        
        self.navigationItem.rightBarButtonItem = addButton
    }
    
    func addStore(){
        performSegueWithIdentifier("toCreateStoreScene", sender: self)
    }
    
    @IBAction func cancelAddStore(sender: UIStoryboardSegue){
        
    }
    
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        
        super.prepareForSegue(segue, sender: sender)
        
        if (segue.identifier != "toCreateStoreScene"){
            return
        }
    }

